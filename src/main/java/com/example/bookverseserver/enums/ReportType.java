package com.example.bookverseserver.enums;

/**
 * Types of user reports.
 */
public enum ReportType {
    COUNTERFEIT,           // Fake product
    FRAUD,                 // Scam/fraudulent activity
    HARASSMENT,            // Abusive behavior
    SPAM,                  // Unsolicited content
    INAPPROPRIATE_CONTENT, // Content that violates guidelines
    POLICY_VIOLATION,      // Seller policy violation
    SAFETY_CONCERN,        // Product safety issue
    WRONG_CATEGORY,        // Listing in wrong category
    MISLEADING_INFO,       // Incorrect or misleading information
    OTHER                  // Other violations
}
